// Function to parse Railway's database configuration
if (!function_exists('parseRailwayDatabase')) {
    function parseRailwayDatabase() {
        // Try MYSQL_URL first, then individual variables
        $url = env('MYSQL_URL') ?: env('DATABASE_URL');
        
        if ($url) {
            $parsedUrl = parse_url($url);
            return [
                'host' => $parsedUrl['host'] ?? '127.0.0.1',
                'port' => $parsedUrl['port'] ?? 3306,
                'database' => ltrim($parsedUrl['path'] ?? '', '/'),
                'username' => $parsedUrl['user'] ?? '',
                'password' => $parsedUrl['pass'] ?? '',
            ];
        }
        
        // Fallback to Railway's individual MySQL variables
        return [
            'host' => env('MYSQLHOST', env('DB_HOST', '127.0.0.1')),
            'port' => env('MYSQLPORT', env('DB_PORT', '3306')),
            'database' => env('MYSQLDATABASE', env('MYSQL_DATABASE', env('DB_DATABASE', 'railway'))),
            'username' => env('MYSQLUSER', env('DB_USERNAME', 'root')),
            'password' => env('MYSQLPASSWORD', env('MYSQL_ROOT_PASSWORD', env('DB_PASSWORD', ''))),
        ];
    }
}

